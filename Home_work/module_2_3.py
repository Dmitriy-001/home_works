# Исходный список
my_list = [42, 69, 322, 13, 0, 99, -5, 9, 8, 7, -6, 5]

# Начальный индекс
index = 0

# Цикл while с условием, чтобы не выйти за границы списка
while index < len(my_list):
    # Получаем текущее число
    num = my_list[index]

    # Проверяем, отрицательное ли число; если да — прерываем цикл
    if num < 0:
        break

    # Если число равно нулю, пропускаем текущую итерацию и продолжаем
    if num == 0:
        index += 1
        continue

    # Если число положительное, выводим его
    print(num)

    # Переходим к следующему индексу
    index += 1
